<script>
$('.modal-next').on("click", function() {
    $('.remodal-wrapper.remodal-is-opened').next().css("display", "block").removeClass('remodal-is-closed').addClass('remodal-is-opened');

    $('.remodal-wrapper.remodal-is-opened').prev().removeClass('remodal-is-opened').addClass('remodal-is-closed').css("display", "none");
    $('.remodal-overlay').css("display", "block").removeClass('remodal-is-closed').addClass('remodal-is-opened');
});

$('.modal-prev').on("click", function() {
    $('.remodal-wrapper.remodal-is-opened').prev().css("display", "block").removeClass('remodal-is-closed').addClass('remodal-is-opened');

    $('.remodal-wrapper.remodal-is-opened').next().removeClass('remodal-is-opened').addClass('remodal-is-closed').css("display", "none");
    $('.remodal-overlay').css("display", "block").removeClass('remodal-is-closed').addClass('remodal-is-opened');
});

$("html").keydown(function(e) {
    if(e.keyCode == 37) { // left
        $('.remodal-wrapper.remodal-is-opened').prev().css("display", "block").removeClass('remodal-is-closed').addClass('remodal-is-opened');

        $('.remodal-wrapper.remodal-is-opened').next().removeClass('remodal-is-opened').addClass('remodal-is-closed').css("display", "none");
        $('.remodal-overlay').css("display", "block").removeClass('remodal-is-closed').addClass('remodal-is-opened');
    }
    else if(e.keyCode == 39) { // right
        $('.remodal-wrapper.remodal-is-opened').next().css("display", "block").removeClass('remodal-is-closed').addClass('remodal-is-opened');

        $('.remodal-wrapper.remodal-is-opened').prev().removeClass('remodal-is-opened').addClass('remodal-is-closed').css("display", "none");
        $('.remodal-overlay').css("display", "block").removeClass('remodal-is-closed').addClass('remodal-is-opened');
    }
});

